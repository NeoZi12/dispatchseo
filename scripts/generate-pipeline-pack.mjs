#!/usr/bin/env node
// Regenerates src/lib/pipeline-pack.json - the repo-side shim files that
// get_pipeline_pack serves to the `install` workflow - from the canonical
// templates in templates/pipeline/ (paths mirror the target repo, so the
// workflow files live under templates/pipeline/.github/ - use `ls -a`).
//
// The templates carry {{SITE_NAME}} / {{DOMAIN}} / {{BACKEND_URL}}
// placeholders; getPipelinePack() substitutes them per project at serve
// time. `pnpm build` runs this before `next build`, so the served pack can
// never drift from the templates - editing a template is enough.
//
//   node scripts/generate-pipeline-pack.mjs           # regenerate
//   node scripts/generate-pipeline-pack.mjs --check   # exit 1 if stale
//
// Historical note: the pack used to be generated from a wired-up reference
// repo (ClockedCode). That made connected repos the source of truth and the
// JSON a hand-refreshed snapshot - a fix could land in one repo and never
// reach new installs. Now the flow is one-way: edit templates here, every
// deploy serves them, connected repos pick them up on their next install.

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const templatesDir = join(root, "templates", "pipeline");
const outPath = join(root, "src", "lib", "pipeline-pack.json");

function walk(dir) {
  const entries = [];
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    if (e.name === "README.md" && dir === templatesDir) continue; // dir doc, not a shim file
    const full = join(dir, e.name);
    if (e.isDirectory()) entries.push(...walk(full));
    else entries.push(full);
  }
  return entries;
}

const files = walk(templatesDir)
  .map((full) => ({
    path: relative(templatesDir, full).split(sep).join("/"),
    content: readFileSync(full, "utf8"),
  }))
  .sort((a, b) => a.path.localeCompare(b.path));

const out = {
  note:
    "GENERATED by scripts/generate-pipeline-pack.mjs from templates/pipeline/ " +
    "- do not edit by hand. Edit the templates and rebuild (pnpm build regenerates).",
  files,
};

const next = JSON.stringify(out, null, 2) + "\n";

if (process.argv.includes("--check")) {
  const current = readFileSync(outPath, "utf8");
  if (current !== next) {
    console.error(
      "pipeline-pack.json is stale - run `node scripts/generate-pipeline-pack.mjs` (or `pnpm build`).",
    );
    process.exit(1);
  }
  console.log("pipeline-pack.json is in sync with templates/pipeline/.");
} else {
  writeFileSync(outPath, next);
  console.log(`pipeline-pack.json: ${files.length} files from templates/pipeline/`);
}
