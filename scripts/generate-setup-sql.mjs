#!/usr/bin/env node
// Concatenates every supabase/migrations/*.sql into a single idempotent
// supabase/migrations/setup.sql (all migrations already use IF NOT EXISTS /
// additive DDL, so re-running is safe). The /setup wizard points fresh
// installs at this ONE file instead of 26 copy-paste cycles - the 2026-07-20
// audit found the old flow only verified the last file's table, so a
// mid-sequence paste slip read as "fully migrated". Runs as part of
// `pnpm build`, so the file can never drift from the individual migrations.
//
//   node scripts/generate-setup-sql.mjs           # regenerate
//   node scripts/generate-setup-sql.mjs --check   # exit 1 if stale

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dir = join(root, "supabase", "migrations");
const outPath = join(dir, "setup.sql");

const files = readdirSync(dir)
  .filter((f) => /^\d{4}_.*\.sql$/.test(f))
  .sort();

const parts = [
  "-- GENERATED by scripts/generate-setup-sql.mjs - do not edit by hand.",
  "-- Every numbered migration, concatenated in order. Idempotent: safe to",
  "-- run on a fresh database AND on one that already has some migrations",
  "-- applied (each statement is IF NOT EXISTS / additive).",
  "",
];
for (const f of files) {
  parts.push(`-- ============ ${f} ============`);
  parts.push(readFileSync(join(dir, f), "utf8").trimEnd());
  parts.push("");
}
const next = parts.join("\n") + "\n";

if (process.argv.includes("--check")) {
  let current = "";
  try {
    current = readFileSync(outPath, "utf8");
  } catch {
    /* missing = stale */
  }
  if (current !== next) {
    console.error("setup.sql is stale - run `node scripts/generate-setup-sql.mjs` (or `pnpm build`).");
    process.exit(1);
  }
  console.log("setup.sql is in sync with the numbered migrations.");
} else {
  writeFileSync(outPath, next);
  console.log(`setup.sql: ${files.length} migrations concatenated.`);
}
